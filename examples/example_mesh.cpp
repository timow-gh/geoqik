#include <GeoQik/GeoQik.hpp>
#include <cstdint>
#include <vector>

int main()
{
  geoqik_init();
  geoqik_draw();

  // Cube vertices
  std::vector<float> vertices = {
    -1.0f, -1.0f,  1.0f,
     1.0f, -1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
    -1.0f,  1.0f,  1.0f,
     1.0f, -1.0f, -1.0f,
    -1.0f, -1.0f, -1.0f,
    -1.0f,  1.0f, -1.0f,
     1.0f,  1.0f, -1.0f,
    -1.0f, -1.0f, -1.0f,
    -1.0f, -1.0f,  1.0f,
    -1.0f,  1.0f,  1.0f,
    -1.0f,  1.0f, -1.0f,
     1.0f, -1.0f,  1.0f,
     1.0f, -1.0f, -1.0f,
     1.0f,  1.0f, -1.0f,
     1.0f,  1.0f,  1.0f,
    -1.0f,  1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
     1.0f,  1.0f, -1.0f,
    -1.0f,  1.0f, -1.0f,
    -1.0f, -1.0f, -1.0f,
     1.0f, -1.0f, -1.0f,
     1.0f, -1.0f,  1.0f,
    -1.0f, -1.0f,  1.0f,
  };

  std::vector<uint32_t> indices = {
     0,  1,  2,   2,  3,  0,
     4,  5,  6,   6,  7,  4,
     8,  9, 10,  10, 11,  8,
    12, 13, 14,  14, 15, 12,
    16, 17, 18,  18, 19, 16,
    20, 21, 22,  22, 23, 20,
  };

  float color[4] = {0.8f, 0.6f, 0.4f, 1.0f};
  geoqik_add_mesh_opts_t opts{};
  opts.color      = color;
  opts.colorCount = 4;
  geoqik_add_mesh_opts(vertices.data(),
                        vertices.size() / 3,
                        indices.data(),
                        indices.size() / 3,
                        &opts);

  geoqik_wait_for_exit_and_cleanup();
  return 0;
}
